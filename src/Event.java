import java.util.*;

public class Event {

    private String name;
    private Date date;




    public String getName(){
        return this.name;
    }


    public Date getDate(){
        return this.date;
    }

    public void setName(String name){
        this.name = name;
    }


    public void setDate(Date date){
        this.date = date;
    }


}